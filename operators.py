#Arithmetic operators -used for simple calculations

X=34
Y=20



print(X+Y)
print(X-Y)
print(X*Y)
print(X/Y)
print(X%Y) #Modulus - Returns the remainder after division


#Comparison operators- Compare values

print( X < Y)
print( X > Y)
print( X <= Y)
print( X >= Y)
print( X == Y) #equal to
print( X != Y) #Not equal to

#ASSIGNMENT OPERATORS
number=10
print(number)

#Operator precedence
number += 2


print(number)

number -= 2
print(number)


#Logical operators - and, or, not
num1=60
num2=12

print(num1 == num2 and num1 > num2)
print(num1 == num2 or num1 > num2)
print(not(num1 == num2 or num1 > num2))

